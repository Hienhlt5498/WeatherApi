package com.example.ThoiTiet.dataTriggers;

public class Condition {
    private String name;
    private String expression;
    private int amount;
    private String _id;

    public Condition() {
    }

    public String getName() {
        return name;
    }

    public String getExpression() {
        return expression;
    }

    public int getAmount() {
        return amount;
    }

    public String get_id() {
        return _id;
    }
}
